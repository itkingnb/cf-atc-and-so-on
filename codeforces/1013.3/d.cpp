ll qpow(ll a, ll k, ll p)
{
    ll res = 1;
    while (k)
    {
        if (k & 1)
            res = (ll)res * a % p;
        a = (ll)a * a % p;
        k >>= 1;
    }
    return res;
}
void solve(){
    ll n,m,k;
    cin>>n>>m>>k;
    ll lo=0,hi=1e9+10;
    auto check=[&](int x)->bool{
        ll t=m/(x+1);
        ll left=m-t*(x+1);
        return n*(left+t*x)>=k;
    };  
    while(lo<hi){
       ll mid=(lo+hi)>>1;
       if(check(mid)){
            hi=mid-1;
       }else{
            lo=mid+1;
       }
    }
    check(hi)?cout<<hi:cout<<hi+1;
}

