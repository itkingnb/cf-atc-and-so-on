ll qpow(ll a, ll k, ll p)
{
    ll res = 1;
    while (k)
    {
        if (k & 1)
            res = (ll)res * a % p;
        a = (ll)a * a % p;
        k >>= 1;
    }
    return res;
}

void solve(){
    ll n;
    cin>>n;
    ll ans=0;
    ll mod=1e9+7;
    ll cnt=0;
    ll sum=0;
    vector<ll>a(n);
    for(int i=0;i<n;i++){
        cin>>a[i];
        sum+=a[i];
    }
    for(int i=0;i<n;i++){
        sum-=a[i];
        ans+=sum%mod*a[i];
        ans%=mod;
        cnt+=n-i-1;
    }
    cnt%=mod;
    cout<<ans*qpow(cnt,mod-2,mod)%mod;
}

