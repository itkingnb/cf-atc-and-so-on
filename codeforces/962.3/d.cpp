ll qpow(ll a, ll k, ll p)
{
    ll res = 1;
    while (k)
    {
        if (k & 1)
            res = (ll)res * a % p;
        a = (ll)a * a % p;
        k >>= 1;
    }
    return res;
}
void solve(){
    int n,x;
    cin>>n>>x;
    ll ans=0;
    for(int a=1;a<=min(x/3,(int)sqrt(n/3));a++){
        for(int b=a;b<=min((x-a)/2,n/a);b++){
            int c=min(x-a-b,(n-a*b)/(a+b));
            if(c>=b){
                if(a==b){
                    ans+=1+3*(c-a);
                }
                else
                ans+=3+6*(c-b);
            }
        }
    }    
    cout<<ans;
}

